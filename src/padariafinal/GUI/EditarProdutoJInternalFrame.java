    private void editarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarBtnActionPerformed
         int linhaSelecionadaProdutos = produtosTable.getSelectedRow();
      
      if(linhaSelecionadaProdutos==-1){
           JOptionPane.showMessageDialog(rootPane, "Selecione um Cliente da tabela!");
        return;  
      }
          
      
      int idProduto = (int) produtosTable.getValueAt(linhaSelecionadaProdutos, 0);
      String nome = (String) produtosTable.getValueAt(linhaSelecionadaProdutos, 1);
      String descricao = (String) produtosTable.getValueAt(linhaSelecionadaProdutos, 2);
      String precoStr = (String) produtosTable.getValueAt(linhaSelecionadaProdutos,3);
      float preco = Float.parseFloat(precoStr);
      
      
      
      Produto produto = new Produto(idProduto,nome,descricao, preco );
      ProdutoDB produtoDB = new ProdutoDB();
      if(produtoDB.update(produto)){
          JOptionPane.showMessageDialog(rootPane, "Produto atualizado com sucesso!");
          produtosTable.clearSelection();
      }else{
          JOptionPane.showMessageDialog(rootPane, "Houve um problema em atualizar os dados!");
      }
      
      
    }//GEN-LAST:event_editarBtnActionPerformed


