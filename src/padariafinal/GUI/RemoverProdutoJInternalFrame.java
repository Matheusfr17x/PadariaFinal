    private void removerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removerBtnActionPerformed
        int linhaSelecionadaProdutos = produtosTable.getSelectedRow();

        if (linhaSelecionadaProdutos == -1) {
            JOptionPane.showMessageDialog(rootPane, "Selecione um Produto da tabela!");
            return;
        }

        int idProduto = (int) produtosTable.getValueAt(linhaSelecionadaProdutos, 0);

        ProdutoDB produtoDB = new ProdutoDB();
        if (produtoDB.hasVenda(idProduto)) {
            int res = JOptionPane.showConfirmDialog(rootPane, "Este produto possui uma ou mais vendas, exclua suas vendas para poder excluir o Produto");
            if (res == JOptionPane.YES_OPTION) {
                if (produtoDB.delete(idProduto)) {
                    JOptionPane.showMessageDialog(rootPane, "Produto Removido com sucesso!");
                    modelProdutoTable.removeRow(linhaSelecionadaProdutos);
                    produtosTable.setModel(modelProdutoTable);
                    jScrollPane1.setViewportView(produtosTable);
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Houve um problema em Remover os dados!");
                }

            }
        } else {
            if (produtoDB.delete(idProduto)) {
                JOptionPane.showMessageDialog(rootPane, "Produto Removido com sucesso!");
                modelProdutoTable.removeRow(linhaSelecionadaProdutos);
                produtosTable.setModel(modelProdutoTable);
                jScrollPane1.setViewportView(produtosTable);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Houve um problema em Remover os dados!");
            }
        }        // TODO add your handling code here:
    }//GEN-LAST:event_removerBtnActionPerformed

