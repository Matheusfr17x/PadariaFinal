    private void loadProdutosTable() {

        modelProdutoTable.addColumn("IdProduto");
        modelProdutoTable.addColumn("Nome");
        modelProdutoTable.addColumn("Descricao");
        modelProdutoTable.addColumn("Preco");

        ProdutoDB produtoDB = new ProdutoDB();
        ArrayList<Produto> produtos = produtoDB.getProdutos();

        for (int i = 0; i < produtos.size(); i++) {
            Produto produto = (Produto) produtos.get(i);
            int idProduto = produto.getIdProduto();
            String nome = produto.getNome();
            String descricao = produto.getDescricao();
            float preco = produto.getPreco();

            modelProdutoTable.addRow(new Object[]{idProduto, nome, descricao, preco});
        }
        produtosTable.setModel(modelProdutoTable);
        jScrollPane1.setViewportView(produtosTable);
    }

